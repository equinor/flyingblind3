from collections import deque
import copy
class Path:
    def __init__(self):
        self.path = []

    def __repr__(self):
        return str(self.path)

def get_neighbors(point, map):

    neigh = [(point[0], point[1] + 1),
             (point[0], point[1] - 1),
             (point[0] + 1, point[1]),
             (point[0] - 1, point[1])
             ]
    valid_neigh = []
    for pt in neigh:
        if pt[0] >= 0 and pt[0] < len(map) and pt[1] >= 0 and pt[1] < len(map[0]):
            if map[pt[0]][pt[1]] == 0:
                valid_neigh.append(pt)

    return valid_neigh


def bfs(map, start, goal):
    seen = [] # Seen positions, first path enters. If new path hits seen, pop it
    queue = deque()
    start_path = Path()
    start_path.path.append(start)
    queue.append(start_path)

    while len(queue) > 0:
        current = queue.popleft()
        #print("Current path: " + str(current))
        seen.append(current.path[-1])
        if (current.path[-1] == goal):
            print("Done!")
            print("Length = " + str(len(current.path)))
            return current.path

        neighbors = get_neighbors(current.path[-1], map)
        for neigh in neighbors:
            if not neigh in seen:
                #print("New neighbor: " + str(neigh))
                neigh_path = copy.deepcopy(current)
                neigh_path.path.append(neigh)
                #print("New path in queue: " + str(neigh_path))
                queue.append(neigh_path)
                seen.append(neigh)

        #print("Current queue: " + str(queue))

def trim(path):
    trimpoint = 0
    candpoint = 0
    trimmed = []
    trimmed.append(path[trimpoint])
    last_skipped = None
    while trimpoint < len(path) - 1:
        candpoint += 1
        trimcord = path[trimpoint]
        candidate = path[candpoint]
        if (trimcord[0] == candidate[0] or trimcord[1] == candidate[1]):
            #dont add anything, but keep the last "un-added"
            last_skipped = candidate
        else:
            trimmed.append(last_skipped)
            trimmed.append(candidate)
            trimpoint = candpoint

    if trimmed[-1] != path[-1]:
        trimmed.append(path[-1])

    return trimmed

